package Polymorphisum;

public class Test2 {
	//CompileTime polymorphisum: Method Overloading
	// method Declaration +   //Method definition/implementation=Compile Time
	
	public void m1()                          // method Declaration
	{
		System.out.println("Hi");             //Method definition/implementation
	}
	
	public void m1(float Z)                          // method Declaration
	{
		System.out.println("Hi");           		//Method definition/implementation
	}
	
	public void m1(int a)                          // method Declaration
	{
		System.out.println("Hello");           		//Method definition/implementation
	}
	
	public void m1(String Sname)                          // method Declaration
	{
		System.out.println("Good Morning");           		//Method definition/implementation
	}	
	
	public void m1(char X)                          // method Declaration
	{
		                                      //Method definition/implementation
	}
	
	public void m1(int z,int y)                          // method Declaration
	{
		System.out.println("Hi");           //Method definition/implementation
	}

}
