package Loops;

public class while1 {
	
	//Print no's from 1 to 5---->1,2,3,4,5
	public static void main(String[] args) 
	{
		     //i=1
		    int i=1;      			//Starting condition
		
		    //1<=5
		    //2<=5
		    //3<=5
		    //4<=5
		    //5<=5
		    //6<=5
		while(i<=5)       			//end condition
		{
			System.out.println(i);  //1   2   3    4   5
			//6
			i++;         			//increment/ decrement		
		}
	}
}
