package Loops;

public class for_loop7 {
	
	//Print no's from 1 to 3---->1,2,3
	public static void main(String[] args) 
	{
		      //i=1      1<=3        2
		                //2<=3       3
		                //3<=3       4
		                //4<=3
		for(int i=1;     i<=3;      i++)                 //i=i+1----->i++
		{
			System.out.println(i);                       //1  2  3
		}
	}
}
