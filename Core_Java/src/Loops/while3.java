package Loops;

public class while3 {
	
	//Print no's from 3 to 1---->3,2,1
	public static void main(String[] args) 
	{        
		//i=3
		int i=3;                	   //Starting condition
		
		     //3>=1
		     //2>=1
		     //1>=1
		     //0>=1
		while(i>=1)                    //end condition
		{
			System.out.println(i);     //3  2  1
			  //0
			 i--;      				   //Increment/ Decrement
		 }   
	}
}
